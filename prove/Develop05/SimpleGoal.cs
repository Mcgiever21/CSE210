namespace GoalSetter
{

    public class SimpleGoal
    {



        public override string ToString()
        {
            String s = "";
            s += $"name: {GetName{}}\n";
            s += $"desc: {GetDesc{}}\n";
            s += $"points: {GetPoints{}}\n";
            s += $"name: {GetName{}}\n";
            s += $"name: {GetName{}}\n";
            s += $"name: {GetName{}}\n";
        }
    }


}